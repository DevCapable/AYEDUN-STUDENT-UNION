                        <h5 style=" box-shadow: 0px 2px 15px 2px gray;border-radius: 100px;"> THE THREE (3) OGBONS IN
                            AYEDUN</h5>
                        <p>
                            Ayedun Community has three (3) administrative wards called Ogbon(Section) i.e. <strong>
                                Ogbon
                                woye, Ogbon Ikotun and Ogbon Odo-lla.</strong> This arrangement is similar to what
                            is obtained in
                            many other communities around us and is even supported by modern politics and
                            administrative where our Local Government Area has three (3) districts and every State
                            in Nigeria has three (3) Senatorial Districts. These arrangements though leans more on
                            administrative convenience, it definitely emanated from cultural and traditional
                            lineage, resulting from clannish affinity and contiguity arrangements. This issue has
                            earlier been treated into details in a report tagged "Observations of the people of
                            Ogbon Ikotun on the communiqué issued by Ogbon Iwoye representatives on their meeting
                            with HRM Obajisun of Ayedun on 24" April, 2011".
                            The area concerning the issue on Ogbon in the report is reproduced here for clarity. The
                            existence of three (3) Ogbons (Section) as administrative bodies was by our traditions
                            which has made the Ogbons spiritual divisions that is now entrenched in our custom
                        </p>
