                        <i class="fa fa-hand-point-right"></i> <strong>INTRODUCTION</strong><br>

                        <p>Ayedun town in Oke - Ero LGA of kwara state, Nigeria came into existence as an autonomous
                            sovereign community in the pre-historic (undated) time around the time
                            other communities (towns and villages) in this part of the world evolved.</p>
                        <p>Like other communities in the Yoruba land, Ayedun was established by a conglomerate of people
                            from different places of
                            the same historical origin. In this case lle-lfe the cradle of the Yoruba race.</p>
                            <i class="fa fa-hand-point-right"></i> <strong>ESTABLISHMENT</strong><br>

