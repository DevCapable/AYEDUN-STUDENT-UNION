                            OBAJISUN OF AYEDUN- THE PARAMOUNT RULER</h5>
                        <p> Out of the federating units that formed Ayedun as highlighted above, it
                            was only the Ikotun man - Ojisan Made the first Obajisun of Ayedun that
                            came in search of a place of suzerainty when he met the Oluodo of Mojo
                            the first settler in Ayedun. Oluodo who was impressed by the carriage
                            of ObaOjisun made and his numerous entourage and who at that time was
                            looking for more people to settle with to expand his
                            abode readily acceded to the request of Oba Ojisunmade to be the Oba of
                            the settlement as a condition for his staying with him.</p>
                        <p>
                            The settlement was then christened Aye Oja after the last settlement of
                            Oba Ojisunmade on his way to Ayedun at Aye in Ekiti States will soon be seen in this paper.
