                            two hunter - Oluodo and Oba Oye: Though it is on record that Olu- Eregun was
                            already an ally of Oluodo having met and lived side by side at Oke Elejola
                            prior to their migration to the present site. Thirteen different sets a migrating
                            people (families/clans) either by accident or by providence met at different times
                            in Ayedun and agreed to live together today. It must be mentioned that different
                            reasons caused the migration of people in those days</p>

